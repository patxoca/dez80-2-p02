;;; dibuixar el terra
;;;
;;; # roig (11), @ groc (10), % cyan (01), . blau (00)
;;; #### -> 1111 1111 -> FF
;;; @%@% -> 1010 0101 -> A5
;;; %@%@ -> 0101 1010 -> 5A
;;; #### -> 1111 1111 -> FF

p02r31::
    ld hl, #0xc000
    ld (hl), #0xff
    ld a, h
    add #8
    ld h, a
    ld (hl), #0xa5
    add #8
    ld h, a
    ld (hl), #0x5a
    add #8
    ld h, a
    ld (hl), #0xff
    ret

